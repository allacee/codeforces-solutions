package main

import (
	"bufio"
	"fmt"
	"os"
	"sort"
)

var in, out = bufio.NewReader(os.Stdin), bufio.NewWriter(os.Stdout)

func min(x, y int) int {
	if x < y {
		return x
	}
	return y
}

func max(x, y int) int {
	if x > y {
		return x
	}
	return y
}

// source: https://codeforces.com/contest/1772/problem/D
func solve() {
	n, arr := _readArr()
	if sort.IntsAreSorted(arr) {
		_print(0)
		return
	}

	rarr := make([]int, n)
	copy(rarr, arr)
	_reverseInts(rarr)
	if sort.IntsAreSorted(rarr) {
		_print(1000000000)
		return
	}

	l, h := 0, int(1000000000)
	for i := 1; i < n; i++ {
		if arr[i] == arr[i-1] {
			continue
		}
		m := arr[i-1] + (arr[i]-arr[i-1])/2
		if arr[i] > arr[i-1] {
			h = min(h, m)
		} else {
			l = max(l, m)
		}
	}
	if l > h {
		_print(-1)
	} else {
		_print(l)
	}

}

